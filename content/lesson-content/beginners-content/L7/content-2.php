		<h2>Third Declension Adjectives</h2>
		<p>These have the same endings as third declension nouns except that adjectives have</p>
		<ul>
			<li>‘-i’ for ablative singular</li>
			<li>‘-ium’ for genitive plural</li>
			<li>‘-ia’ for nominative, vocative and accusative neuter plural</li>
		</ul>
		<p>There are two main forms of third declension adjetives</p>
		<p><strong>1. Masculine and feminine are same; neuter is different</strong></p>

