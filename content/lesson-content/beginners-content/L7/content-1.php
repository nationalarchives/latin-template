		<h2>Third declension nouns</h2>
		<p>You can identify third declension nouns by their <b>genitive singular</b> ending ‘-<b>is</b>’.</p>
		<p>You cannot identify third declension nouns in the nominative because they</p>
		<ul>
			<li>Have various forms and spelling</li>
			<li>Have endings that do not reveal their gender</li>
			<li>Can be masculine, feminine or neuter</li>
		</ul>
		<p>To decline a third declension noun:</p>
		<ul>
			<li>Find the <b>genitive singular</b>, which will end ‘-is’</li>
			<li>Remove the ‘-is’, leaving you with the <b>stem</b></li>
			<li>Add the <b>endings</b> shown below</li>
		</ul>
