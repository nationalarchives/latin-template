        <h2>Introducing new nouns</h2>
        <p>From now on, new nouns (hyperlink needed for nouns) will be written as they appear in the word list.</p>
        <p>They will be given in the nominative (hyperlink) singular, followed by the genitive (hyperlink) singular ending. 
        The gender (hyperlink) of the noun will be given and its meaning in English. For example: </p>
        <p><em>marca, -e</em> (f.) mark</p>
        <p>The genitive <strong>singular</strong> is very important, as it tells you which declension (hyperlink) the noun belongs to.</p>
