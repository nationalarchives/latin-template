    <div class="entry-header">
        <h2>Past, Present and Imperfect</h2>
    </div>
    <div class="entry-content clearfix">

        <p>In Beginners’ Latin we looked at <a href="">verbs</a> in the <a href="">present tense</a>.</p>

        <p>For example, present tense <em>Ego Lucie nuntium do</em> means "I <strong>give</strong> a message to Lucy".</p>

        <p>We also looked at verbs in the <a href="">past tense</a>.</p>

        <p>For example, past tense (perfect) <em>Ego Lucie nuntium dedi</em> means "I <strong>gave</strong> a message to Lucy".</p>

        <p>This kind of past tense is called a <a href="">perfect tense</a>. It is used to describe an action in the past which is completed.</p>
        <p>To describe a past action or state which is incomplete, we use an imperfect tense. This tense indicates an action which has gone on over a period of time or has
            happened frequently. It is translated into English by "was/were" + "-ing" or "used to".</p>

        <p>For example, past tense (imperfect) <em>Ego Lucie nuntium dabam</em> means "I <strong>was giving</strong> a message to Lucy".</p>
        
        <p>Just as with the present and perfect tenses, we need to know the <a href="">conjugation</a> a verb belongs to in order to make an imperfect tense.</p>    

        <div class="helpbox">
            <h4>Remember</h4>
            <p>Latin verbs are divided into four groups, or conjugations.</p>
            <p>In each conjugation, the verbs share the same endings:</p>
            <p>An example of a first conjugation verb is: confirmo, confirmare, confirmavi, confirmatum (1) - to confirm.<br/>
               An example of a second conjugation verb is: habeo, habere, habui, habitum (2) - to have<br/>
               An example of a third conjugation verb is: duco, ducere, duxi, ductum (3) - to lead<br />
               An example of a fourth conjugation verb is: audio, audire, audivi, auditum (4) - to hear</p>

        </div>

