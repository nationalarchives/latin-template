        <div class="helpbox">
            <strong>Handy hint</strong>
            <p>Two <a href="">irregular</a> verbs you will frequently come across are <em>‘esse’</em>, <em>‘to be’</em>, and <em>‘ire’</em>, <em>‘to go’</em>. It is well worth learning their imperfect forms.
            </p>
        </div>

