							<h2>Word order in Latin.</h2>
							<p>Think about the order that words are arranged in sentences.</p>
							<div class="row-element clearfix">
								<div class="col-md-6">
									<table class="table table-bordered text-center ">
										<!-- Items -->
										<tbody>
											<tr>
												<td>In English:</td>
												<td>‘I give land’.</td>
											</tr>
											<tr>
												<td>The word order is:</td>
												<td>subject (I) + verb (give) + object (land).</td>
											</tr>
										</tbody>
									</table>
								</div>
								<div class="col-md-6">
									<table class="table table-bordered  text-center">
										<!-- Items -->
										<tbody>
											<tr>
												<td>The Latin translation is:</td>
												<td><em>terram do.</em></td>
											</tr>
											<tr>
												<td>The word order is:</td>
												<td>object (land, <em>terram</em>) + verb (I give, <em>do</em>).</td>
											</tr>
										</tbody>
									</table>
								</div>
							</div>
							<p>The subject of the Latin sentence is expressed through the verb <em>do</em>, which by itself simply means 'I give'.</p>

							<p>You may often find that in Latin:</p>
							<ul>
								<li>The subject is at the beginning of the sentence</li>
								<li>The verb is at the end of the sentence</li>
								<li>The object of the sentence follows the subject</li>
							</ul>

							<div class="row-element clearfix">
								<div class="col-md-2">
									<p>For example:</p>
								</div>
								<div class="col-md-6">
									<table class="table table-bordered text-center">
										<!-- Items -->
										<tbody>
											<tr>
												<td><em>domine cartas dant.</em></td>
												<td>‘the ladies give charters’.</td>
											</tr>
											<tr>
												<td>The word order is:</td>
												<td>subject (the ladies) + object (charters) + verb (give).</td>
											</tr>
										</tbody>
									</table>
								</div>
							</div>

							<p>However, these rules were not always applied and vary between documents. You may find that the word order is different in your document. It may even be in the same order as English.</p>
							<p>Look out for sentences that do and don’t keep to these rules during the tutorial.</p>
