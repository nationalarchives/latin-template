        <p>Some third conjugation verbs are called ‘<strong>io</strong>’ verbs because they have different endings</p>
        <ul>
            <li>-io for the ‘I’ form (instead of –o)</li>
            <li>-iunt for the ‘they’ form (instead of –unt)</li>
        </ul>
        <p>You are most likely to come across <em>facere</em> (to make, do), an example of which can be seen in: <em>novum testamentum facio</em> meaning 'I make a new will'.</p>

