        <h2>Negative sentences</h2>
        <p>It's easy to turn a sentence into a negative sentence - simply place <em>non</em> at the end of the verb:</p>

        <table class="table text-center">
            <!-- Headings -->
            <thead>
            <tr>
                <th>Latin</th>
                <th>English</th>
            </tr>
            </thead>
            <!-- Items -->
            <tbody>
            <tr>
                <td><em>agricola laborat</em></td>
                <td>The farmer works</td>
            </tr>
            <tr>
                <td><em>agricola non laborat</em></td>
                <td>The farmer does not work</td>
            </tr>
            <tr>
                <td><em>regina cartas confirmat</em></td>
                <td>The queen confirms the charters</td>
            </tr>
            <tr>
                <td><em>regina cartas non confirmat</em></td>
                <td>The queen does not confirm the charters</td>
            </tr>
            </tbody>
        </table>
