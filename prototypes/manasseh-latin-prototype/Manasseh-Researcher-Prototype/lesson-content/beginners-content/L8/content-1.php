		<h2>Prepositions</h2>
        <h3>What is a Preposition?</h3>
           <p>A Preposition is a word that goes in front of a noun. The preposition does not decline, 
              <br>but it changes the case of the noun that follows it. 
              Concentrate on learning words marked with an asterisk* first.</br>
              Most prepositions are followed by a noun in the accusative or the ablative case.
              Some can be followed by a noun in either case, <br>depending on their meaning.
           </p>
