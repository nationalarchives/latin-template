        <h3 id="HandyHints">Handy Hints</h3>
        <p>Learn noun endings quickly by looking for <strong>patterns</strong>:</p>
        <ul>
            <li>Nominative and vocative endings are always the same except for second declension nouns ending in ‘us’.</li>
            <li>Nominative and accusative cases of neuter nouns are always the same. The plural always ends in ‘a’.</li>
            <li>Accusative singular for masculine and feminine nouns always ends in ‘m’; accusative plural for masculine and feminine nouns always ends in ‘s’.</li>
            <li>Genitive plural of all declensions ends in ‘um’.</li>
            <li>Dative and ablative plurals are always the same. In the first and second declensions, the ending is usually ‘is’.</li>
        </ul>

