        <h3 id="MascERend">Masculine ‘er’ ending</h3>

        <p>These take the same endings as masculine ‘us’ ending nouns, except for:</p>
        <ul>
            <li>Nominative singular ends in ‘er’</li>
            <li>Vocative singular is the same as the nominative</li>
        </ul>

        <p>You will only come across these four words below. You can see their declined form in the grammar table.</p>

        <table class="table text-center">
            <thead>
            <th>Latin</th>
            <th>English</th>
            </thead>
            <tbody>
            <tr>
                <td><em>armiger</em></td>
                <td>esquire</td>
            </tr>
            <tr>
                <td><em>faber</em></td>
                <td>smith</td>
            </tr>
            <tr>
                <td><em>magister</em></td>
                <td>master, teacher</td>
            </tr>
            <tr>
                <td><em>puer</em></td>
                <td>boy</td>
            </tr>
            </tbody>
        </table>

        <p>They can be divided into two groups:</p>

        <div class="row-element clearfix">
            <div class="col-md-6">
                <p><strong>First:</strong></p>
                <p><em>puer</em> and <em>armiger</em>, which always keep their ‘e’ when they are declined.</p>
            </div>
            <div class="col-md-6">
                <p><strong>Second:</strong></p>
                <p><em>magister</em> and <em>faber</em>. When they are declined, they only keep the ‘e’ of the ‘er’ in the nominative and vocative singular cases.</p>
            </div>
        </div>

        <p>For example:</p>
        <table class="table text-center">
            <thead>
            <th>Latin</th>
            <th>English</th>
            </thead>
            <tbody>
            <tr>
                <td><em>faber terras legat</em></td>
                <td>The smith bequeaths lands
                </td>
            </tr>
            </tbody>
        </table>

        <p>In the above sentence, <em>faber</em> has an ‘e’ because it is in the nominative case.</p>

        <table class="table text-center">
            <thead>
            <th>Latin</th>
            <th>English</th>
            </thead>
            <tbody>
            <tr>
                <td><em>cartas magistro Stephano confirmo</em></td>
                <td>I confirm charters to master Stephen</td>
            </tr>
            </tbody>
        </table>

        <p>However, In this sentence <em>magister</em> has lost its ‘e’, because it is in the dative case.</p>
        <p>Tip: Thinking of related English words may help you remember this: magistrate and fabricate do not have an ‘e’.</p>

