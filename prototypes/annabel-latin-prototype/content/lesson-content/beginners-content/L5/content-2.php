		<h2>Irregular adjectives</h2>
		<p>There are nine irregular adjectives. These decline like <em>novus</em>, -<em>a</em>, -<em>um</em> <b>but</b></p>
		<ul>
			<li>Genitive singular ends ‘-ius’</li>
			<li>Dative singular ends ‘–i’ for all genders</li>
		</ul>
		<p> You are most likely to find <em>unus</em>, <em>totus</em> and <em>alius</em>, so remember these three. </p>
		<table class="table text-center">
			<!-- Items -->
			<tbody>
				<tr>
					<td><em>alius , alia, aliud</em></td>
					<td>the other [genitive singular is sometimes alterius]*</td>
				</tr>
				<tr>
					<td><em>alter , altera, alterum</em></td>
					<td>the other (of two things)</td>
				</tr>
				<tr>
					<td><em>neuter , neutra, neutrum</em></td>
					<td>neither (of two things)</td>
				</tr>
				<tr>
					<td><em>nullus , -a, -um</em></td>
					<td>no, none</td>
				</tr>
				<tr>
					<td><em>solus , -a, -um</em></td>
					<td>alone, only</td>
				</tr>
