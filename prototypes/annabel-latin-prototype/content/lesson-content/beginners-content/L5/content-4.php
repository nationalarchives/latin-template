		<h2><em>Ego</em> and <em>Nos</em></h2>
		<p> Usually, Latin verbs do not need separate words for ‘I’, ‘you’, ‘he’, ‘she’, ‘it’, ‘we’ or ‘they’. However, they are sometimes used to add emphasis, particularly at the beginning of grants. Ego and nos are called <a href ="http://www.nationalarchives.gov.uk/latin/beginners/popup/grammar/pronouns.htm#personal">personal pronouns</a> since they stand in the place of a person. </p>

<h3>Latin document points</h3>
<p>You are only likely to come across <em>ego</em> ‘I’ and <em>nos</em> ‘we’.</p>

