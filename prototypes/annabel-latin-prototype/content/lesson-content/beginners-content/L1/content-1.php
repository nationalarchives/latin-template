        <h2>Introduction to Verbs</h2>

        <h3>What is a verb?</h3>
        <p>A verb is a word that describes an action. Verbs are often called ‘doing’ words. Examples of verbs: to work, to call, to pray, to be, to think.</p>

        <h3>First conjugation verbs</h3>
        <p>Verbs are divided into groups called conjugations. You can recognise first conjugation verbs as they end ‘are’.</p>
