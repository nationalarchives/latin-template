                                verb. In Latin, however, these words are usually unnecessary. Damus means 'we give'; A
                                separate word for ‘we’ is not needed.</p>

                            <p>In Latin, there are two ways of saying ‘you’. For example, both das and datis mean ‘you
                                give’. The form used depends on the number of people:</p>
