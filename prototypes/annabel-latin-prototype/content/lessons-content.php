                            <h2>Differences between English and Latin verbs</h2>

                            <p>Let's look at the verb 'to give'. In English, the ending of the verb changes once: it
                                acquires an 's' in 'he gives'. In Latin, the ending of the verb changes every time. The
                                ending of the verb in Latin is crucial as it tells you the:</p>
                            <ul>
                                <li><em>Tense</em> (when the action happens - the present tense)</li>
                                <li><em>Person</em> (who is doing the action - I, you, he, she, it, we or they)</li>
