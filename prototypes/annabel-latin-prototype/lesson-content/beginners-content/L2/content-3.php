						<div class="entry-content clearfix">
							<h2>The Six Cases of Nouns</h2>

							<p>A noun has six different meanings, each relating to a case. Each case then has a singular and a plural ending.</p>
							<hr>
							<!-- 1.NOMINATIVE -->
							<div class="row-element clearfix">
								<div class="col-md-3">
									<h3>1. Nominative</h3>
								</div>
								<div class="col-md-9">
									<p>Used for the <strong>subject</strong> of the verb. The subject is the person or thing doing the verb.</p>
									<p>For example:</p>
									<table class="table text-center">
										<!-- Headings -->
										<thead>
											<tr>
												<th><em>Latin</em></th>
												<th>English</th> 
											</tr>
										</thead>
										<!-- Items -->
										<tbody>
											<tr>
												<td><em>regina orat</em></td>
												<td>the queen prays</td>
											</tr>
										</tbody>
									</table>
									<p>The queen is the subject, as she is praying. The queen is in the nominative case.</p>
								</div>
							</div>
							<hr>
							<!-- 2.VOCATIVE -->
							<div class="row-element clearfix">
								<div class="col-md-3">
									<h3>2. Vocative</h3>
								</div>
								<div class="col-md-9">
									<p>Used to call or <strong>address</strong> someone or something.</p>
									<p>For example:</p>
									<table class="table text-center">
										<!-- Headings -->
										<thead>
											<tr>
												<th><em>Latin</em></th>
												<th>English</th> 
											</tr>
										</thead>
										<!-- Items -->
										<tbody>
											<tr>
												<td><em>O domina!</em></td>
												<td>O lady!</td>
											</tr>
											<tr>
												<td><em>O regina!</em></td>
												<td>O queen!</td>
											</tr>
											<tr>
												<td><em>O Maria!</em></td>
												<td>O Mary!</td>
											</tr>
										</tbody>
									</table>
									<p>The vocative case is the same as the nominative, except in the second declension (which we shall learn more about later).</p>
									<p>(The vocative case is used in chronicles and in the inscriptions on tombs. You will not find it in many other sources.)</p>
								</div>
							</div>
							<hr>
							<!-- 3.ACCUSATIVE -->
							<div class="row-element clearfix">
								<div class="col-md-3">
									<h3>3. Accusative</h3>
								</div>
								<div class="col-md-9">

								</p>Used for the <strong>object</strong> of a verb. The object is the person or thing the verb is done to.</p>
								<p>For example:</p>
								<table class="table text-center">
									<!-- Headings -->
									<thead>
										<tr>
											<th><em>Latin</em></th>
											<th>English</th> 
										</tr>
									</thead>
									<!-- Items -->
									<tbody>
										<tr>
											<td><em>domina cartam confirmat.</em></td>
											<td>The lady confirms the charter.</td>
										</tr>
									</tbody>
								</table>
								<p>‘the lady’ is the subject and in the nominative. ‘the charter’ is the object and in the accusative.</p>
							</div>
						</div>
						<hr>
						<!-- 4. GENITIVE -->
						<div class="row-element clearfix">
							<div class="col-md-3">
								<h3>4. Genitive</h3>
							</div>
							<div class="col-md-9">
								<p>Used for nouns that are ‘<strong>of</strong>’ something else and also to show <strong>possession</strong>.</p>
								<p>For example:</p>
								<table class="table text-center">
									<!-- Headings -->
									<thead>
										<tr>
											<th><em>Latin</em></th>
											<th>English</th> 
										</tr>
									</thead>
									<!-- Items -->
									<tbody>
										<tr>
											<td><em>terra ecclesie.</em></td>
											<td>The land of the church.</td>
										</tr>
										<tr>
											<td><em>filie vidue.</em></td>
											<td>The widow’s daughters.</td>
										</tr>
									</tbody>
								</table>
							</div>
						</div>
						<hr>
						<!-- 5. DATIVE -->
						<div class="row-element clearfix">
							<div class="col-md-3">
								<h3>4. Dative</h3>
							</div>
							<div class="col-md-9">
								<p>Used for nouns that are <strong>to</strong> or <strong>for</strong> something.</p>
								<p>For example:</p>
								<table class="table text-center">
									<!-- Headings -->
									<thead>
										<tr>
											<th><em>Latin</em></th>
											<th>English</th> 
										</tr>
									</thead>
									<!-- Items -->
									<tbody>
										<tr>
											<td><em>terram ecclesie do.</em></td>
											<td>I give land to the church.</td>
										</tr>
									</tbody>
								</table>
								<p>‘I give’ is the verb – do. ‘land’ is the object – it is in the accusative. ‘to the church’ is in the dative.</p>
							</div>
						</div>
						<hr>
						<!-- 6.ABLATIVE -->
						<div class="row-element clearfix">
							<div class="col-md-3">
								<h3>6. Ablative</h3>
							</div>
							<div class="col-md-9">
								<p>Used for nouns that are <strong>by</strong>, <strong>with</strong> or <strong>from</strong> something.</p>
								<p>For example:</p>
								<table class="table text-center">
									<!-- Headings -->
									<thead>
										<tr>
											<th><em>Latin</em></th>
											<th>English</th> 
										</tr>
									</thead>
									<!-- Items -->
									<tbody>
										<tr>
											<td><em>papa ecclesiam carta confirmat.</em></td>
											<td>The pope confirms the church by a charter.</td>
										</tr>
									</tbody>
								</table>
								<p>‘the pope’ is the subject – it is in the nominative. ‘confirms’ is the verb.</p>
								<p>‘the church’ is the object – it is in the accusative. ‘by a charter’ is ablative.</p>
							</div>
						</div>
						<hr>

						<p>To decline a noun means to list a noun in the form of all these above cases.</p>

						<p>In certain languages, Latin included, nouns are divided into five groups called declensions. </p>
