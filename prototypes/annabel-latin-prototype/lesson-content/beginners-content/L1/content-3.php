        <h2>Differences between English and Latin verbs</h2>

        <p>Let's look at the verb 'to give'. In English, the ending of the verb changes once: it acquires an 's' in 'he gives'. In Latin, the ending of the verb changes every time. The ending of the verb in Latin is crucial as it tells you the:</p>
        <ul>
            <li><em>Tense</em> (when the action happens - the present tense)</li>
            <li><em>Person</em> (who is doing the action - I, you, he, she, it, we or they)</li>
            <li><em>Number</em> (how many people are doing the verb – if one, then it’s singular; if two or more, it’s plural)</li>
        </ul>
        <p>(You may wish to read through our translation of <em>dare</em> again and notice how the endings change.)</p>

        <p>In English, the words ‘I’, ‘you’, ‘she’, ‘he’, ‘it’, ‘we’ or ‘they’ show who is doing the verb. In Latin, however, these words are usually unnecessary. Damus means 'we give'; A separate word for ‘we’ is not needed.</p>

        <p>In Latin, there are two ways of saying ‘you’. For example, both das and datis mean ‘you give’. The form used depends on the number of people:</p>
        <ul>
            <li>You (singular) when there is one person, for example das</li>
            <li>You (plural) when there are two or more people, for example datis</li>
        </ul>
        <p>There were two forms of ‘you’ in English: 'thou givest' and 'you give' (‘Thou’ is no longer used widely) </p>
        <p>This tutorial concentrates on 'I', 'he/she/it', 'we' and 'they'. These are more likely to appear in records than the two forms of ‘you’. </p>

        <p>In our previous example of <em>dare</em>, the action is happening now – the present tense.</p>
        <p>In English, there are three ways of describing actions in the present: 'I give', 'I am giving', and 'I do give' (All which come under the verb 'to do'). </p>
        <p>In Latin, however, just one word can be used to convey all three meanings. Latin is much more concise than English.</p>

