        <h3 id="HandyHints">Handy Hints</h3>
        <p>All verbs that end with:</p>
        <ul>
            <li><em>-o</em> relate to I</li>
            <li><em>-t</em> relate to he/she/it</li>
            <li><em>-mus</em> relate to we</li>
            <li><em>-nt</em> relate to they</li>
        </ul>
        <p>Remember - this applies to <strong>all</strong> conjugations.</p>
