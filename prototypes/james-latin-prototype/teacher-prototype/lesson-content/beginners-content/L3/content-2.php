        <h2><em>Esse</em> - to be</h2>

        <p>This is an irregular verb, both in English and in Latin, as it does not follow the usual patterns of conjugation.</p>

        <table class="table text-center">
            <!-- Headings -->
            <thead>
            <tr>
                <th><em>Latin</em></th>
                <th>English</th>
            </tr>
            </thead>
            <!-- Items -->
            <tbody>
            <tr>
                <td><em>sum</em></td>
                <td>I am</td>
            </tr>
            <tr>
                <td><em>es</em></td>
                <td>you are (singular)</td>
            </tr>
            <tr>

                <td><em>est</em></td>
                <td>he/she/it is</td>
            </tr>
            <tr>

                <td><em>sumus</em></td>
                <td>we are</td>
            </tr>
            <tr>
                <td><em>estis</em></td>
                <td>you are (plural)</td>
            </tr>
            <tr>
                <td><em>sunt</em></td>
                <td>they are</td>
            </tr>
            </tbody>
        </table>

        <p><em>esse</em> <strong>does not have an object</strong>. Words associated with it are in the <a href="http://www.nationalarchives.gov.uk/latin/beginners/popup/glossary/default.htm#nominative"><em>nominative</em></a> case. Don’t try to put them into the accusative.</p>

