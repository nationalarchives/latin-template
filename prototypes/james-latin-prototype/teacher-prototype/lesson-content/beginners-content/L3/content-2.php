        <p>In this example, <em>Maria</em> and <em>Isabella</em> are nominative singular, as there is one of each woman. <em>regine</em> is nominative plural as there are two queens.</p>

        <h3>Latin document points: medieval names</h3>

        <p>Men’s names often include the phrase ‘son of’:</p>
        <table class="table text-center">
            <!-- Headings -->
            <thead>
            <tr>
                <th><em>Latin</em></th>
                <th>English</th>
            </tr>
            </thead>
            <!-- Items -->
            <tbody>
            <tr>
                <td><em>Henricus filius Willelmi </em></td>
                <td>Henry son of William.</td>
            </tr>
            <tr>
                <td><em>Willelmus filius Henrici</em></td>
                <td>William son of Henry.</td>
            </tr>
            <tr>
                <td><em>cartas Willelmo filio Stephani do</em></td>
                <td>I give charters to William son of Stephen.</td>
            </tr>
            </tbody>
        </table>
        <p><em>Willelmo</em> and <em>filio</em> are both in the dative case because the charters are given <strong>to</strong> him.</p>
        <p><em>Stephani</em> stays in the genitive, because William is the son <strong>of Stephen</strong>.</p>
