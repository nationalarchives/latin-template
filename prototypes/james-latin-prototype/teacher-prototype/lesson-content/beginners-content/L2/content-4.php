						<p>The ending of a noun is <strong>crucial</strong>, as it tells you: </p>
						<ul>
							<li>Whether the noun is singular or plural</li>
							<li>What role the noun is playing in the sentence.</li>
						</ul>
						<p>Read through our example of <em>carta</em> again. Can you see that some endings are the same, but have different meanings? For example, <em>carte</em> can mean:</p>
						<ul>
							<li>Charters</li>
							<li>Of the charter</li>
							<li>To or for the charter</li>
						</ul>
						<p>To decide which meaning is appropriate, one can read the rest of the sentence and infer from there. For example, if part of a sentence is ‘she asked the king’ and the remaining word is carte, the meaning of carte that would make sense is ‘for the charter’.</p>
						<p>All first declension nouns take the same endings as carta <strong>except</strong> for <em>anima</em> (soul) and <em>filia</em> (daughter); Instead <em>animabus</em> means ‘to or for the souls’ and ‘by, with or from the souls’, and <em>filiabus</em> means ‘to or for the daughters’ and ‘by, with or from the daughters’</p>
