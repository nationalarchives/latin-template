						<p>Notice anything similar about them?</p>
						<p>Nouns that end in ‘a’ belong to the <strong>first declension</strong>; They are mostly <strong>feminine</strong>.</p>
						<p>People’s names are also nouns. In Latin, women’s names often end in ‘a’ (example: Maria to Mary).</p>

						<p>All Latin nouns have a gender – they are either masculine, feminine or neuter. Even charters and parishes have a gender! </p>
						<p>In English we give some nouns a gender, for example we describe ships as ‘she’.</p>

