        <h2><em>Hic</em>, <em>Hec</em>, and <em>Hoc</em></h2>


        <p><em>Hic</em>, <em>hec</em>, <em>hoc</em> has several meanings:</p>
        <ul>
            <li>'<strong>This</strong>' when it's an adjective - describing a noun</li>
            <li>'<strong>He</strong>, <strong>she</strong>, <strong>it</strong>' when it's a pronoun - standing in for a noun</li>
        </ul>

        <p><em>hic, hec, hoc</em> agrees with the <strong>noun</strong> it relates to.</p>

