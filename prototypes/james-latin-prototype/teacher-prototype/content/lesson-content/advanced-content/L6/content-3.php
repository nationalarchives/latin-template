        <p>Did you notice any similarities between the endings of regular third and fourth conjugation verbs? Look back at the lesson and check. </p>
        <p>The endings are all the same, with the only differences being that in the fourth conjugation, the ‘I’ and ‘they’ forms have an ‘i’. </p>
        <p>Noticing patterns like this makes learning Latin much easier.</p>

