		<p>An adjective:</p>
		<ul>
			<li>Is normally in front of the noun it describes; sometimes it is behind</li>
			<li>Agrees with the noun in: </li>
			<ol>
				<li><a href ="http://www.nationalarchives.gov.uk/latin/beginners/popup/glossary/default.htm#gender">Gender</a> (masculine, feminine or neuter)</li>
				<li><a href ="http://www.nationalarchives.gov.uk/latin/beginners/popup/glossary/default.htm#number">Number</a> (singular or plural)</li>
				<li><a href ="http://www.nationalarchives.gov.uk/latin/beginners/popup/glossary/default.htm#case">Case</a> (nominative, vocative, accusative, genitive, dative or ablative)</li>
			</ol>
			<li>Belongs to one of two groups depending on whether it declines:</li>
			<ol>
				<li>Like the first and second declension nouns</li>
				<li>Like third declension nouns</li>
			</ol>
		</ul>

