			<p>Look out for adjectives that end ‘<em>er</em>’ in the masculine nominative singular.</p>
			<p>Most lose the ‘e’ when declined, for example    <em>pulcher</em>, <em>pulchra</em>, <em>pulchrum</em>    beautiful </p>
			<p>A few keep the ‘e’, for example    <em>liber</em>, -<em>era</em>, -<em>erum</em>    free</p>
