			<p>Noun and adjective are masculine accusative plural.</p>
			<p><strong>Don’t</strong> assume that the ending of the noun and the adjective are always the same. Sometimes they are different, for example:</p>
			<table class="table text-center">
				<!-- Items -->
				<tbody>
					<tr>
						<td><em>novus agricola est</em></td>
						<td>He is the new farmer.</td>
					</tr>
				</tbody>
			</table>
			<p>Noun and adjective are both masculine nominative singular.</p>
			<br>
			<p>In the word list, first and second declension adjectives are written ‘novus, -a, -um’ This shows the three nominative singular forms:</p>
			<ul>
				<li><em>novus</em> is masculine and declines like <em>dominus</em> from the second declension</li>
				<li><em>nova</em> is feminine and declines like <em>carta</em> from the first declension</li>
				<li><em>novum</em> is neuter and declines like <em>testamentum</em> from the second declension</li>
			</ul>
			<p>When you see an adjective written like this, you will know that it is first or second declension. You will be able to decline it using the same endings as novus.</p>

