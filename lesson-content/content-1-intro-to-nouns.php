		<h2>Introduction to Nouns</h2>
		<h3>What is a noun?</h3>
		<p>A <a href="http://www.nationalarchives.gov.uk/latin/beginners/popup/glossary/default.htm#noun">noun</a> is a person, place or thing; For example: the queen, a parish, the charter.</p> 
		<p>However, to contrast with the English language, Latin has no words for ‘a’ or ‘the’; For example: <em>regina</em> means 'queen','the queen', and 'a queen' and <em>carta</em> means 'charter', 'the charter', and 'a charter' (When you are reading a document, you can decide which meaning is most appropriate).</p>
